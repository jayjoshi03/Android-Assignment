package kotlin_Fundamental

import java.util.Scanner

fun main() {
    val sc = Scanner(System.`in`)

//    ---Multiple Floating Number---

    print("Enter a Float Num1: ")
    val num1 = sc.nextFloat()
    print("Enter a Float Num2: ")
    val num2 = sc.nextFloat()
    println("Multiply: ${num1 * num2}")

}